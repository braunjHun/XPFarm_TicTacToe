public class Player {

    private String playerName;
    private char playerSign;

    public Player(String playerName, char playerSign) {
        this.playerName = playerName;
        this.playerSign = playerSign;
    }


    public String getPlayerName() {
        return playerName;
    }


    public char getPlayerSign() {
        return playerSign;
    }

  
    
    
}
